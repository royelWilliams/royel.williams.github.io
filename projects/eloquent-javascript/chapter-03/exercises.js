function min(a,b) {
  if( a>b){
    return b;
  }
  else{ return a;}
  

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num === 0){
    return true;
  }
  else{
    return (-num % 2. === 0) ? true: false;
  }
  return isEven(num - 2);

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars( str,char) {
let count = 0;
for(let i =0;i<str.length;i++){
  if(str[i]== char){
    count += 1;
  }
}
return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var count =0;
  for(var i=0;i<string.length;i+=1){
    if(string.charAt(i)=== "B")
    count += 1;
  }
  return count;
}
